	private Carta(int numCarta, tipusMoviment opcio, boolean isEspecial) {
		this.numCarta = numCarta;
		this.opcio = opcio;
		this.isEspecial = isEspecial;
	}

	/*
	 * GENEREM LA BARALLA
	 * 
	 */

	public static List<Carta> generarBaralla() {

		List<Carta> baralla = new ArrayList<>();
		int numCarta;
		tipusMoviment opcioCarta = null;
		boolean especial = false;

		for (int i = 0; i < NUMCARTES; i++) {

			numCarta = i + 1;

			if (i < 28) {
				opcioCarta = tipusMoviment.values()[1];
			} else if (i < 46) {
				opcioCarta = tipusMoviment.values()[2];
			} else if (i < 56) {
				opcioCarta = tipusMoviment.values()[3];
			} else if (i < 59) {
				opcioCarta = tipusMoviment.values()[4];
			} else if (i < 63) {
				opcioCarta = tipusMoviment.values()[5];
			} else if (i < 66) {
				opcioCarta = tipusMoviment.values()[6];
			} else if (i < 68) {
				opcioCarta = tipusMoviment.values()[7];
			} else if (i < 70) {
				opcioCarta = tipusMoviment.values()[8];
			} else if (i < 52) {
				opcioCarta = tipusMoviment.values()[10];
			}

			baralla.add(new Carta(numCarta, opcioCarta, especial));
		}
		Collections.shuffle(baralla);
		return baralla;

	}

