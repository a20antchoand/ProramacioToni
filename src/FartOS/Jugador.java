package FartOS;

import java.util.List;

public class Jugador {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////ATRIBUTS/////////////////////////////////////////////////////////////////////	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	/*
	 * ATRIBUTS FINAL
	 * 
	 * */
	
	private final int vidasEstandard = 5;
	public static int idJugador = 0;
	/*
	 * ATRIBUTS JUGADOR
	 * 
	 * */
	
	private String nom;
	private List<Carta> ma;
	private int pos = 0;
	private int id;
	private boolean estaEspecial = false;
	private int vides;
	private int patada = -1;
	private int zancadilla = -1;
	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	
	
	
	
	
	
	
	
	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////INICIEM JUGADOR//////////////////////////////////////////////////////////////	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	/*
	 * INICIALITZAR JUGADOR
	 * 
	 * */
	
	public Jugador(String nom) {
		this.nom = nom;
		this.vides = vidasEstandard;
		this.id = idJugador++;
	}
	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	
	
	
	
	
	
	
	
	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////GETTERS & SETTERS/////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	
	/*
	 * GETTERS
	 * 
	 * */
	
	public String getNom () {
		return nom;
	}
	
	public List<Carta> getMa () {
		return ma;
	}
	
	public int getPos () {
		return pos;
	}

	public int getId () {
		return id;
	}
	
	public int getVides () {
		return vides;
	}
	
	public int getPatada() {
		return patada;
	}
	
	public int getZancadilla() {
		return zancadilla;
	}
	
	/*
	 * SETTERS
	 * 
	 * */
	
	public void setNom(String nom) {
		this.nom = nom;
	}
	
	public void setMa (List<Carta> ma) {
		this.ma = ma;
	}
	
	public void setPos(int pos) {
		this.pos = pos;
	}
	
	public void setVides (int vides) {
		this.vides = vides;
	}

	public void setPatada(int patada) {
		this.patada = patada;
	}
	
	public void setZancadilla(int zancadilla) {
		this.zancadilla = zancadilla;
	}
	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	
	
	public boolean teCartes(List<Carta> list) {
		if (getMa().size() == 0) {
			return false;
		}
		return true;
	}
	

	
	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////TOSTRING/////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	/*
	 * TO STRING
	 * 
	 * */
	
	@Override
	public String toString () {
		return "Nom: " + nom + " / Vidas: " + vides;
	}

	public boolean isEstaEspecial() {
		return estaEspecial;
	}

	public void setEstaEspecial(boolean estaEspecial) {
		this.estaEspecial = estaEspecial;
	}
	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
}
